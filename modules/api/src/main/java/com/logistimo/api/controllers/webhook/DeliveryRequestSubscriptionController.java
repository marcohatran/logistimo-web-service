package com.logistimo.api.controllers.webhook;

import com.logistimo.api.action.webhook.UpdateDeliveryRequestAction;
import com.logistimo.deliveryrequest.models.webhook.WebhookEvent;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.http.HttpStatus;
import org.springframework.http.ResponseEntity;
import org.springframework.stereotype.Controller;
import org.springframework.web.bind.annotation.RequestBody;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestMethod;

/**
 * Created by kumargaurav on 25/06/19.
 */
@Controller
@RequestMapping(path = "/delivery-requests-subscription")
public class DeliveryRequestSubscriptionController {

  @Autowired
  private UpdateDeliveryRequestAction processWebhookAction;


  @RequestMapping(method = RequestMethod.POST)
  public ResponseEntity processUpdate(@RequestBody WebhookEvent webhook)  {

    processWebhookAction.invoke(webhook);
    return new ResponseEntity<>("Webhook processed successfully", HttpStatus.OK);
  }

}
